# Variáveis de exemplo
x <- 10
y <- 3

# Soma
soma <- x + y

# Subtração
subtracao <- x - y

# Multiplicação
multiplicacao <- x * y

# Divisão
divisao <- x / y

# Impressão dos resultados
print(paste("Soma:", soma))
print(paste("Subtração:", subtracao))
print(paste("Multiplicação:", multiplicacao))
print(paste("Divisão:", divisao))

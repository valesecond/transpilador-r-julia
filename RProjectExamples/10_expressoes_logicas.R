# Variáveis de exemplo
a <- TRUE
b <- FALSE

# Operador E (AND)
resultado_e <- a & b    # TRUE se ambos forem TRUE

# Operador OU (OR)
resultado_ou <- a | b   # TRUE se pelo menos um for TRUE

# Operador NÃO (NOT)
resultado_nao <- !a     # Inverte o valor lógico

# Impressão dos resultados
print(paste("E:", resultado_e))
print(paste("OU:", resultado_ou))
print(paste("NÃO:", resultado_nao))
